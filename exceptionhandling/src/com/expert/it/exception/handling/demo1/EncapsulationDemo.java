package com.expert.it.exception.handling.demo1;

public class EncapsulationDemo {
int id; // internaly maintain for storing record 
String name;
String age;
String status;
String dateofregi;

public int getId() {  
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getAge() {
	return age;
}
public void setAge(String age) {
	this.age = age;
}
public String getStatus() {
	return status;
}
public void setStatus(String status) {
	this.status = status;
}
public String getDateofregi() {
	return dateofregi;
}
public void setDateofregi(String dateofregi) {
	this.dateofregi = dateofregi;
}





}
